class Node
{
	private int data;
	private Node left;
	private Node right;

	public Node getLeft()
	{
		return this.left;
	}

	public void setLeft(Node left)
	{
		this.left = left;
	}

	public int getData()
	{
		return this.data;
	}

	public void setData(int data)
	{
		this.data = data;
	}

	public Node getRight()
	{
		return this.right;
	}

	public void setRight(Node right)
	{
		this.right = right;
	}
}